def translate_line(line):
    # 解析行結構，例如 "1. [TAG: Name] 草原" -> prefix="1. [TAG: Name] ", text="草原"
    # 或者 "15. [MARKER: Message] \S[10]貴様らごときに..." -> prefix="15. [MARKER: Message] ", text="..."
    if "] " in line:
        parts = line.split("] ", 1)
        prefix = parts[0] + "] "
        content = parts[1]
    else:
        prefix = ""
        content = line

    # 查表替換
    if content in DB_MAP:
        return prefix + DB_MAP[content]
    
    # 部分比對（針對人名或含有括號的細節等）
    stripped = content.strip()
    if stripped in DB_MAP:
        # 保留原本的頭尾空格
        l_spaces = len(content) - len(content.lstrip())
        r_spaces = len(content) - len(content.rstrip())
        return prefix + (" " * l_spaces) + DB_MAP[stripped] + (" " * r_spaces)

    # 如果沒在字典中，試圖做簡單的字詞局部替換（例如帶有「」或空格的內容）
    # 對於沒對照到的，我們就原樣輸出或給出警告
    return line

