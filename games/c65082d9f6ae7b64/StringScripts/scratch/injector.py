import os
import re

def inject_translation(origin_path, output_path, translations):
    with open(origin_path, 'r', encoding='utf-8-sig') as f:
        content = f.read()

    # 使用正則表達式尋找 #Message# 區塊
    msg_blocks = re.findall(r'#Message#\n(.*?)\n##', content, re.DOTALL)
    
    if len(msg_blocks) != len(translations):
        print(f"Warning: Translation count mismatch! Origin: {len(msg_blocks)}, Trans: {len(translations)}")
    
    # 替換邏輯
    def replace_func(match):
        nonlocal counter
        if counter < len(translations):
            new_text = translations[counter]
            counter += 1
            return f"#Message#\n{new_text}\n##"
        return match.group(0)

    counter = 0
    new_content = re.sub(r'#Message#\n(.*?)\n##', replace_func, content, flags=re.DOTALL)
    
    with open(output_path, 'w', encoding='utf-8-sig') as f:
        f.write(new_content)

